#arithmetic operators
a = 23
b = 12
c = 4
x = 10

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(b%c) #returns remainder after division

#comparison operators
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
print(a==b) #equal
print(a!=b) #not equal

#assignment operators
print(x)
x /= 3
print(x)

#logical operators - and not or
print(36>30 and a<20)
print(36>30 or a<20)
print(not(26>30 or a<20))
print(not(26>30 and a<20))